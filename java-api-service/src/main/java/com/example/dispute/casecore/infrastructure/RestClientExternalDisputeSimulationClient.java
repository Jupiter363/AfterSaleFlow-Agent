    private static final String ENDPOINT = "/internal/agents/external-import/simulate";

    private final RestClient restClient;

    // 所属模块：【案件核心与导入 / 外部集成层】「RestClientExternalDisputeSimulationClient.RestClientExternalDisputeSimulationClient(RestClient)」。
