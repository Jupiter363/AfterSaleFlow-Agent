    // 所属模块：【案件核心与导入 / 外部集成层】「RestClientExternalDisputeSimulationClient.simulate(SimulateExternalImportCommand,String,String)」。
    // 具体功能：「RestClientExternalDisputeSimulationClient.simulate(SimulateExternalImportCommand,String,String)」：模拟列表：先调用受配置和超时控制的内部 HTTP 服务；实际协作者为 「restClient.post」、「AgentSimulationRequest.from」、「response.items」、「retrieve」；不满足前置条件时抛出 「AgentExecutionException」；处理的关键状态/协议值包括 「endpoint」、「item_count」、「validation_error」，最终返回「List<SimulatedExternalDispute>」。
    // 上游调用：「RestClientExternalDisputeSimulationClient.simulate(SimulateExternalImportCommand,String,String)」由使用「RestClientExternalDisputeSimulationClient」的控制器、应用服务、Workflow Activity 或测试场景触发。
    // 下游影响：「RestClientExternalDisputeSimulationClient.simulate(SimulateExternalImportCommand,String,String)」向下依次触达 「restClient.post」、「AgentSimulationRequest.from」、「response.items」、「retrieve」；计算结果以「List<SimulatedExternalDispute>」交给调用方。
    // 系统意义：「RestClientExternalDisputeSimulationClient.simulate(SimulateExternalImportCommand,String,String)」负责主链路中的“列表”；Java/PostgreSQL 是案件状态事实源，导入重试不能创建重复案件
    @Override
    public List<SimulatedExternalDispute> simulate(
            SimulateExternalImportCommand command,
            String traceId,
            String requestId) {
        SimulationResponse response =
                restClient
                        .post()
                        .uri(ENDPOINT)
                        .header(TraceIdFilter.TRACE_HEADER, traceId)
                        .header(TraceIdFilter.REQUEST_HEADER, requestId)
                        .contentType(MediaType.APPLICATION_JSON)
                        .body(AgentSimulationRequest.from(command))
                        .retrieve()
                        .body(SimulationResponse.class);
        int itemCount =
                response == null || response.items() == null
                        ? 0
                        : response.items().size();
        if (itemCount != 1) {
            throw new AgentExecutionException(
                    ErrorCode.AGENT_OUTPUT_SCHEMA_INVALID,
                    "external import simulator must return exactly one item",
                    Map.of("endpoint", ENDPOINT, "item_count", itemCount));
        }
        try {
            return response.items().stream()
                    .map(AgentSimulatedExternalDispute::toDomain)
                    .toList();
        } catch (IllegalArgumentException invalidItem) {
            throw new AgentExecutionException(
                    ErrorCode.AGENT_OUTPUT_SCHEMA_INVALID,
                    "external import simulator returned an invalid item",
                    Map.of(
                            "endpoint",
                            ENDPOINT,
                            "validation_error",
                            invalidItem.getMessage()));
        }
    }

    // 所属模块：【案件核心与导入 / 外部集成层】类型「AgentSimulationRequest」。
    // 类型职责：定义Agent模拟跨层传递时使用的不可变数据契约；本类型显式提供 「from」。
