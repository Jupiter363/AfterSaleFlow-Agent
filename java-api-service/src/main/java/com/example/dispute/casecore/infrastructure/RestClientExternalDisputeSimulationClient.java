    // 下游影响：「RestClientExternalDisputeSimulationClient.RestClientExternalDisputeSimulationClient(RestClient)」只产生当前对象的返回值或字段变化，不访问额外基础设施。
    // 系统意义：「RestClientExternalDisputeSimulationClient.RestClientExternalDisputeSimulationClient(RestClient)」负责主链路中的“Rest客户端外部争议模拟客户端”；Java/PostgreSQL 是案件状态事实源，导入重试不能创建重复案件
